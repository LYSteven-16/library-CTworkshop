var _re = null;
function _getRegex() {
  if (_re) return _re;
  var keys = Object.keys(I18N.zh).filter(function(k) { return I18N.zh[k] !== k && k.length >= 2; });
  keys.sort(function(a, b) { return b.length - a.length; });
  if (!keys.length) return null;
  _re = new RegExp('(' + keys.map(function(k) { return k.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); }).join('|') + ')', 'g');
  return _re;
}

function applyI18n(container) {
  if (currentLang() === 'en') return;
  var re = _getRegex();
  if (!re) return;
  var dict = I18N.zh;
  var count = 0;
  function tr(str) { return str.replace(re, function(m) { count++; return dict[m] || m; }); }
  var w = document.createTreeWalker(container, NodeFilter.SHOW_TEXT, null, false);
  var n;
  while (n = w.nextNode()) {
    var txt = n.nodeValue;
    if (txt.trim().length && /[a-zA-Z]{2}/.test(txt)) {
      var m = txt.match(/^(\s*)([\s\S]*?)(\s*)$/);
      var c = m ? m[2] : txt;
      var r = tr(c);
      if (r !== c) n.nodeValue = (m ? m[1] : '') + r + (m ? m[3] : '');
    }
  }
  container.querySelectorAll('[placeholder]').forEach(function(el) {
    var v = el.getAttribute('placeholder');
    if (v && /[a-zA-Z]{2}/.test(v)) {
      var zh = I18N.zh[v];
      if (zh && zh !== v) el.setAttribute('placeholder', zh);
    }
  });
}

