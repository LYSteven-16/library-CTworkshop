function t(key) {
  var lang = localStorage.getItem('ct_lang') || 'en';
  var dict = I18N[lang];
  if (dict && dict[key] !== undefined) return dict[key];
  return key; // fallback: show key as-is (Chinese)
}

function currentLang() {
  return localStorage.getItem('ct_lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('ct_lang', lang);
}

/* Apply translations to all text nodes in container (recursively) */
function applyI18n(container) {
  var walker = document.createTreeWalker(container, NodeFilter.SHOW_TEXT, null, false);
  var node;
  while (node = walker.nextNode()) {
    var text = node.nodeValue.trim();
    if (text.length > 0 && /[\u4e00-\u9fff]/.test(text)) {
      // Try exact match first
      var translated = t(text);
      if (translated !== text) {
        // Preserve leading/trailing whitespace
        var ws = node.nodeValue.match(/^(\s*)([\s\S]*?)(\s*)$/);
        if (ws) {
          node.nodeValue = ws[1] + translated + ws[3];
        } else {
          node.nodeValue = translated;
        }
      }
    }
  }
  // Also translate alt/title/placeholder attributes
  container.querySelectorAll('[alt], [title], [placeholder]').forEach(function(el) {
    ['alt', 'title', 'placeholder'].forEach(function(attr) {
      var val = el.getAttribute(attr);
      if (val && /[\u4e00-\u9fff]/.test(val)) {
        var tval = t(val);
        if (tval !== val) el.setAttribute(attr, tval);
      }
    });
  });
}
