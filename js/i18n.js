function t(key) {
  var lang = localStorage.getItem('ct_lang') || 'en';
  var dict = I18N[lang];
  if (dict && dict[key] !== undefined) return dict[key];
  // Fallback: regex substring matching
  if (typeof _i18nRegex === 'undefined') _buildI18nRegex();
  if (_i18nRegex) return key.replace(_i18nRegex, function(match) { return dict[match] || match; });
  return key;
}

var _i18nRegex = null;
function _buildI18nRegex() {
  var dict = I18N.en;
  var keys = Object.keys(dict).filter(function(k) { return dict[k] !== k; });
  keys.sort(function(a, b) { return b.length - a.length; });
  if (keys.length === 0) return;
  var escaped = keys.map(function(k) { return k.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); });
  _i18nRegex = new RegExp('(' + escaped.join('|') + ')', 'g');
}

function currentLang() {
  return localStorage.getItem('ct_lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('ct_lang', lang);
}

function applyI18n(container) {
  var lang = currentLang();
  if (lang === 'zh') return;
  if (typeof _i18nRegex === 'undefined') _buildI18nRegex();
  var dict = I18N.en;

  function translateText(str) {
    if (!_i18nRegex) return str;
    return str.replace(_i18nRegex, function(match) { return dict[match] || match; });
  }
  var walker = document.createTreeWalker(container, NodeFilter.SHOW_TEXT, null, false);
  var node;
  while (node = walker.nextNode()) {
    var text = node.nodeValue;
    if (text.trim().length > 0 && /[\u4e00-\u9fff]/.test(text)) {
      var ws = text.match(/^(\s*)([\s\S]*?)(\s*)$/);
      var core = ws ? ws[2] : text;
      var translated = translateText(core);
      if (translated !== core) {
        node.nodeValue = (ws ? ws[1] : '') + translated + (ws ? ws[3] : '');
      }
    }
  }
  container.querySelectorAll('[alt], [title], [placeholder]').forEach(function(el) {
    ['alt', 'title', 'placeholder'].forEach(function(attr) {
      var val = el.getAttribute(attr);
      if (val && /[\u4e00-\u9fff]/.test(val)) {
        var tval = t(val);
        if (tval !== val) el.setAttribute(attr, tval);
      }
    });
  });
}

